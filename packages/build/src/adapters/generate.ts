import { writeFile, mkdir } from "node:fs/promises";
import { join } from "node:path";
import type { AdaptPlan } from "./types";

const ADAPTED_DIR = ".agentd";

function slugify(s: string) {
  return s.toLowerCase().trim().replace(/[^a-z0-9]+/g, "-").replace(/(^-|-$)/g, "");
}

function bridgeHandler(plan: AdaptPlan): string {
  const entry = `../${plan.entryPath.replace(/\\/g, "/")}`;
  const exp = plan.entryExport;

  switch (plan.framework) {
    case "langgraph":
      return `
  const mod = await import(${JSON.stringify(entry)});
  const graph = ${exp ? `mod[${JSON.stringify(exp)}]` : "mod.graph ?? mod.default"};
  if (!graph?.invoke) throw new Error("LangGraph export must expose .invoke()");
  const payload = typeof input.message === "string" ? input : { message: JSON.stringify(input) };
  const result = await graph.invoke({
    messages: [{ role: "user", content: payload.message }],
  });
  const messages = result?.messages ?? [];
  const last = messages[messages.length - 1];
  const text = typeof last?.content === "string" ? last.content : JSON.stringify(result);
  return { reply: text, result };`;

    case "openai-agents":
      return `
  const mod = await import(${JSON.stringify(entry)});
  const agent = ${exp ? `mod[${JSON.stringify(exp)}]` : "mod.agent ?? mod.default"};
  const { Runner } = await import("@openai/agents");
  if (!agent) throw new Error("OpenAI Agents export not found");
  const runner = new Runner();
  const msg = typeof input.message === "string" ? input.message : JSON.stringify(input);
  const run = await runner.run(agent, msg);
  const text = typeof run?.finalOutput === "string" ? run.finalOutput : JSON.stringify(run);
  return { reply: text, result: run };`;

    case "mastra":
      return `
  const mod = await import(${JSON.stringify(entry)});
  const mastra = mod.mastra ?? mod.default;
  const agents = mastra?.getAgents?.() ?? mastra?.agents;
  const first = agents ? Object.values(agents)[0] : mod.agent;
  if (!first?.generate) throw new Error("Mastra agent with .generate() not found");
  const msg = typeof input.message === "string" ? input.message : JSON.stringify(input);
  const result = await first.generate([{ role: "user", content: msg }]);
  const text = result?.text ?? JSON.stringify(result);
  return { reply: text, result };`;

    case "vercel-ai":
      return `
  const mod = await import(${JSON.stringify(entry)});
  const run = mod.runAgent ?? mod.default ?? mod.POST;
  if (typeof run !== "function") throw new Error("Vercel AI entry must export runAgent, default, or POST");
  const msg = typeof input.message === "string" ? input.message : JSON.stringify(input);
  const result = await run({ input: { message: msg }, ctx });
  if (result && typeof result === "object" && "reply" in result) return result;
  const text = result?.text ?? (typeof result === "string" ? result : JSON.stringify(result));
  return { reply: text, result };`;

    default:
      return `
  throw new Error("Unsupported adapted framework");`;
  }
}

/** Write agent.config.ts + README shim so runtime loads without manual defineAgent. */
export async function writeAdaptedAgent(repoDir: string, plan: AdaptPlan): Promise<string> {
  const outDir = join(repoDir, ADAPTED_DIR);
  await mkdir(outDir, { recursive: true });

  const slug = plan.slug || slugify(plan.name);
  const config = `import { defineAgent } from "@platform/sdk";
import { z } from "zod";

/**
 * Generated by agentd (${plan.framework} adapter). Original entry: ${plan.entryPath}
 * Customize input/output schemas after first deploy if needed.
 */
export default defineAgent({
  name: ${JSON.stringify(plan.name)},
  description: ${JSON.stringify(plan.description)},
  input: z.object({ message: z.string() }).passthrough(),
  output: z.object({ reply: z.string(), result: z.unknown().optional() }).passthrough(),
  distribute: ["http", "mcp", "cli", "playground"],
  handler: async (input, ctx) => {${bridgeHandler(plan)}
  },
});
`;

  const configPath = join(outDir, "agent.config.ts");
  await writeFile(configPath, config, "utf8");
  await writeFile(
    join(outDir, "ADAPTER.md"),
    `# Adapted from ${plan.framework}\n\nEntry: \`${plan.entryPath}\`${plan.entryExport ? ` export \`${plan.entryExport}\`` : ""}\n`,
    "utf8",
  );

  return outDir;
}
